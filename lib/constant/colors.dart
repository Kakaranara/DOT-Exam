import 'package:flutter/cupertino.dart';

class ThemeColor {
  static Color blue({double opacity = 1}) {
    return Color.fromRGBO(10, 151, 171, opacity);
  }

  static Color green({double opacity = 1}) {
    return Color.fromRGBO(70, 181, 167, opacity);
  }

  static Color yellow({double opacity = 1}) {
    return Color.fromRGBO(242, 201, 76, opacity);
  }

  static Color lightBlue({double opacity = 1}) {
    return Color.fromRGBO(86, 204, 242, opacity);
  }

  static Color purple({double opacity = 1}) {
    return Color.fromRGBO(155, 81, 224, opacity);
  }
}
